"""Benchmark Random Forest vs XGBoost vs Deep Learning and write a report.

Loads the three trained artifacts, evaluates them on an identical test split,
and produces metrics, confusion matrices, comparison plots, and docs/comparison.md.
"""
import time
import json
import numpy as np
import joblib
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.metrics import (
    classification_report, confusion_matrix, f1_score, accuracy_score,
)
import tensorflow as tf

from ml.config import (
    CLASSES, RF_MODEL_FILE, XGB_MODEL_FILE, DL_MODEL_FILE, DOCS_DIR,
)
from ml.preprocessing.pipeline import prepare, load_dataset

FIG_DIR = DOCS_DIR / "figures"
FIG_DIR.mkdir(parents=True, exist_ok=True)


def _plot_confusion(y_true, y_pred, name: str):
    cm = confusion_matrix(y_true, y_pred, labels=CLASSES)
    plt.figure(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues",
                xticklabels=CLASSES, yticklabels=CLASSES)
    plt.title(f"Confusion Matrix - {name}")
    plt.ylabel("True")
    plt.xlabel("Predicted")
    plt.tight_layout()
    path = FIG_DIR / f"cm_{name.lower().replace(' ', '_')}.png"
    plt.savefig(path)
    plt.close()
    return path.name


def evaluate():
    # Use the same split for every model (fixed seed => identical test set).
    X_tr, X_val, X_te, y_tr, y_val, y_te, _ = prepare(load_dataset())
    y_te = y_te.reset_index(drop=True)

    results = {}

    # --- Random Forest ---
    rf = joblib.load(RF_MODEL_FILE)
    t = time.perf_counter()
    rf_pred = rf.predict(X_te)
    rf_latency = (time.perf_counter() - t) / len(X_te) * 1000  # ms/sample
    results["Random Forest"] = {
        "acc": accuracy_score(y_te, rf_pred),
        "f1": f1_score(y_te, rf_pred, average="macro"),
        "latency_ms": rf_latency,
        "pred": rf_pred,
    }

    # --- XGBoost ---
    bundle = joblib.load(XGB_MODEL_FILE)
    xgb, le = bundle["model"], bundle["label_encoder"]
    t = time.perf_counter()
    xgb_pred = le.inverse_transform(xgb.predict(X_te))
    xgb_latency = (time.perf_counter() - t) / len(X_te) * 1000
    results["XGBoost"] = {
        "acc": accuracy_score(y_te, xgb_pred),
        "f1": f1_score(y_te, xgb_pred, average="macro"),
        "latency_ms": xgb_latency,
        "pred": xgb_pred,
    }

    # --- Deep Learning ---
    dl = tf.keras.models.load_model(DL_MODEL_FILE)
    dl_classes = np.array(json.loads((DL_MODEL_FILE.parent / "dl_classes.json").read_text()))
    t = time.perf_counter()
    dl_pred = dl_classes[np.argmax(dl.predict(X_te, verbose=0), axis=1)]
    dl_latency = (time.perf_counter() - t) / len(X_te) * 1000
    results["Deep Learning"] = {
        "acc": accuracy_score(y_te, dl_pred),
        "f1": f1_score(y_te, dl_pred, average="macro"),
        "latency_ms": dl_latency,
        "pred": dl_pred,
    }

    # Confusion matrices + per-class F1.
    per_class_f1 = {}
    cm_files = {}
    for name, r in results.items():
        cm_files[name] = _plot_confusion(y_te, r["pred"], name)
        per_class_f1[name] = f1_score(
            y_te, r["pred"], average=None, labels=CLASSES
        )

    _plot_per_class_f1(per_class_f1)
    _plot_summary(results)
    _write_report(results, per_class_f1, cm_files)
    print("Wrote docs/comparison.md and figures.")


def _plot_per_class_f1(per_class_f1):
    x = np.arange(len(CLASSES))
    width = 0.25
    plt.figure(figsize=(8, 5))
    for i, (name, scores) in enumerate(per_class_f1.items()):
        plt.bar(x + i * width, scores, width, label=name)
    plt.xticks(x + width, CLASSES)
    plt.ylabel("F1 score")
    plt.title("Per-class F1 by model")
    plt.legend()
    plt.tight_layout()
    plt.savefig(FIG_DIR / "per_class_f1.png")
    plt.close()


def _plot_summary(results):
    names = list(results)
    fig, ax = plt.subplots(1, 2, figsize=(11, 4))
    ax[0].bar(names, [results[n]["f1"] for n in names], color="steelblue")
    ax[0].set_title("Macro F1")
    ax[0].set_ylim(0, 1)
    ax[1].bar(names, [results[n]["latency_ms"] for n in names], color="indianred")
    ax[1].set_title("Inference latency (ms/sample)")
    plt.tight_layout()
    plt.savefig(FIG_DIR / "summary.png")
    plt.close()


def _write_report(results, per_class_f1, cm_files):
    best = max(results, key=lambda n: results[n]["f1"])
    lines = [
        "# Model Comparison: Random Forest vs XGBoost vs Deep Learning\n",
        "Auto-generated by `ml/evaluation/compare.py`.\n",
        "## Summary\n",
        "| Model | Accuracy | Macro F1 | Latency (ms/sample) |",
        "|---|---|---|---|",
    ]
    for n, r in results.items():
        lines.append(
            f"| {n} | {r['acc']:.3f} | {r['f1']:.3f} | {r['latency_ms']:.4f} |"
        )
    lines += [
        f"\n**Recommended production model: {best}** "
        "(highest macro F1; weigh latency and interpretability for your deployment).\n",
        "## Per-class F1\n",
        "![Per-class F1](figures/per_class_f1.png)\n",
        "## Summary charts\n",
        "![Summary](figures/summary.png)\n",
        "## Confusion matrices\n",
    ]
    for n, f in cm_files.items():
        lines.append(f"### {n}\n![{n}](figures/{f})\n")
    lines += [
        "## Trade-offs\n",
        "- **Random Forest**: strong baseline, fast to train, interpretable "
        "(feature importance), robust to unscaled data.\n",
        "- **XGBoost**: usually best accuracy/F1 on tabular intrusion data, "
        "handles imbalance via class weights, slightly less interpretable.\n",
        "- **Deep Learning (MLP)**: scales to large data and complex patterns, "
        "but needs scaling, more tuning, less interpretable, and is often "
        "slower per inference on CPU.\n",
    ]
    (DOCS_DIR / "comparison.md").write_text("\n".join(lines), encoding="utf-8")


if __name__ == "__main__":
    evaluate()
